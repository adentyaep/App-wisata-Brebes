package com.example.appwisata;

public class RestoranModel {
    private String nama_restoran;
    private String lokasiRestoran;
    private String gambarRestoran;
    private String deskripsiResto;
    private String longitudeRestoran;
    private String latitudeRestoran;

    public String getNama_restoran() {
        return nama_restoran;
    }

    public void setNama_restoran(String nama_restoran) {
        this.nama_restoran = nama_restoran;
    }

    public String getLokasiRestoran() {
        return lokasiRestoran;
    }

    public void setLokasiRestoran(String lokasiRestoran) {
        this.lokasiRestoran = lokasiRestoran;
    }

    public String getGambarRestoran() {
        return gambarRestoran;
    }

    public void setGambarRestoran(String gambarRestoran) {
        this.gambarRestoran = gambarRestoran;
    }

    public String getDeskripsiResto() {
        return deskripsiResto;
    }

    public void setDeskripsiResto(String deskripsiResto) {
        this.deskripsiResto = deskripsiResto;
    }

    public String getLongitudeRestoran() {
        return longitudeRestoran;
    }

    public void setLongitudeRestoran(String longitudeRestoran) {
        this.longitudeRestoran = longitudeRestoran;
    }

    public String getLatitudeRestoran() {
        return latitudeRestoran;
    }

    public void setLatitudeRestoran(String latitudeRestoran) {
        this.latitudeRestoran = latitudeRestoran;
    }
}
